import React, { useState } from 'react'
import { assets, facilityIcons, roomsDummyData } from '../assets/assets'
import { useNavigate } from 'react-router-dom'
import StarRating from '../components/StarRating';

const CheckBox =({label,seleted=false,onChange=()=>{}})=>{
    return (
        <label className='flex gap-3 items-center cursor-pointer mt-2 text-sm'>
            <input type='checkbox' checked={seleted} onChange={(e)=>onChange(e.target.checked,label)} />
            <span className='font-light select-none'>{label}</span>
        </label>
    )
}


const RadioButton =({label,seleted=false,onChange=()=>{}})=>{
    return (
        <label className='flex gap-3 items-center cursor-pointer mt-2 text-sm'>
            <input type='radio' checked={seleted} onChange={(e)=>onChange(label)} />
            <span className='font-light select-none'>{label}</span>
        </label>
    )
}




const AllRooom = () => {
    const navigate = useNavigate();
    const [openFilters,setOpenFilters] = useState(false)


const roomTypes = [
    "single Bed",
    "Double Bed",
    "Luxury Room",
    "Family Suite"
]

const priceRanges = [
    "0 to 500",
    "500 to 1000",
    "1000 to 2000",
    "2000 to 3000"
]

const sortOptions = [
    "price low to high",
    "price high to low",
    "newest first"
]


  return (
    <div className='flex flex-col-reverse lg:flex-row items-start justify-between pt-28 mg:pt-35 px-4 md:px-16 lg:px-24 xl:px-32'>
        <div>
            <div>
                <h1 className='text-4xl mg:text-[40px]'>MyStay Rooms.</h1>
                <p className='text-sm md:text-base text-gray-500/90 mt-2'>Take advantage of our limited-time offers and special packages to enhance your stay and create unforgetabble memories.</p>
            </div>

            {roomsDummyData.map((room)=>(
                <div  key={room._id} className='flex flex-col md:flex-row items-start py-10 gap-6 border-b border-gray-300 last:pb-30 last:border-0'>
                    <img
                    onClick={()=>{navigate(`/rooms/${room._id}`);scrollTo(0,0)}}
                     src={room.images[0]} 
                     className='max-h-63 md:w-1/2 rounded-xl shadow-lg object-cover cursor-pointer'
                      title='View Room Details' alt="" />
                      <div className='md:w-1/2 flex flex-col gap-2'>
                        <p className='text-gray-500'>{room.hotel.city}</p>
                        <p  onClick={()=>{navigate(`/rooms/${room._id}`);scrollTo(0,0)}} className='text-gray-800 text-3xl cursor-pointer'>{room.hotel.name}</p>
                        <div className='flex items-center'>
                        <StarRating/>
                        <p className='ml-2 font-light'>200+ Reviews</p>
                        </div>
                        <div className='flex items-center gap-1 text-gray-500 mt-2 text-sm'>
                            <img src={assets.locationIcon} alt="" />
                            <span>{room.hotel.address}</span>
                        </div>

                    {/* room Amentites */}
                        <div className='flex flex-wrap items-center mt-3 mb-6 gap-4 '>
                            {room.amenities.map((item,index)=>(
                                <div key={item._id} className='flex items-center gap-2 px-3 py-2 rounded-lg bg-[#F5F5FF]/70'>
                                    <img src={facilityIcons[item]} alt={item} className='w-5 h-5 ' />
                                    <p className='text-xs'>{item}</p>
                                </div>
                            ))}
                        </div>
                        {/* Room price per Night */}
                        <p className='text-xl font-medium text-gray-800'>${room.pricePerNight} per night</p>
                      </div>
                </div>
            ))}
        </div>

        <div className='bg-white w-80 border border-gray-300 text-gray-600 max-lg:mb-8 lg:mt-16'>
            <div className={`flex items-center justify-between px-5 py-2.5 lg:border-b border-gray-300 ${openFilters && "border-b"}`}>
                <p className='text-base font-medium text-gray-800'>FILTERS</p>
                <div className='text-xs cursor-pointer '>
                    <span onClick={()=>setOpenFilters(!openFilters)} className='lg:hidden'>
                        {openFilters ? 'Hide' : 'SHOW'}
                    </span>
                    <span className='hidden lg:block'>CLEAR</span>
                </div>
            </div>  
                <div className={`${openFilters ? 'h-auto' : "h-auto"} overflow-hidden transition-all duration-700`}>
                   <div className='px-5 pt-5'>
                        <p className='font-medium text-gray-800 pb-2'>Popular filters</p>
                        {roomTypes.map((room,index)=>(
                            <CheckBox key={index} label={room}/>
                        ))}
                    </div>

                     <div className='px-5 pt-5'>
                        <p className='font-medium text-gray-800 pb-2'>price range</p>
                        {priceRanges.map((range,index)=>(
                            <CheckBox key={index} label={` $ ${range}`}/>
                        ))}
                    </div>

                     <div className='px-5 pt-5'>
                        <p className='font-medium text-gray-800 pb-2'>sort by</p>
                        {sortOptions.map((option,index)=>(
                          <RadioButton key={index} label={option}/>
                        ))}
                    </div>
                </div>

        </div>
        </div>
  )
}

export default AllRooom

